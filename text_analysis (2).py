def count_syllables(word):
    word = word.lower().strip()
    if len(word) <= 3:
        return 1
    # remove trailing 'es' or 'ed' sometimes not syllabic
    word = re.sub(r'[^a-z]', '', word)
    word = re.sub(r'(es|ed)$', '', word)
    # count vowel groups
    groups = re.findall(r'[aeiouy]+', word)
    syllables = len(groups)
    # ensure at least 1
    return syllables if syllables > 0 else 1

def is_complex(word):
    return count_syllables(word) > 2

def readability_metrics(words, sentences):
    word_count = len(words)
    sentence_count = max(len(sentences), 1)
    avg_sentence_length = word_count / sentence_count
    complex_count = sum(1 for w in words if is_complex(w))
    pct_complex = complex_count / (word_count + 1e-6)
    fog_index = 0.4 * (avg_sentence_length + (pct_complex * 100))  # careful: doc's % maybe decimal or percent
    # NOTE: the original doc says Percentage of Complex words = number complex / number words
    # Fog formula in doc: 0.4*(Avg sentence length + Percentage of Complex words)
    # If Percentage is decimal (0.12) then Fog small; many sources use percentage (12), so both appear.
    # Use doc literal: if document intends decimal, remove *100 above.
    return avg_sentence_length, pct_complex, fog_index, complex_count, word_count

import string

def clean_words(text):
    tokens = word_tokenize(text)
    words = []
    for t in tokens:
        w = t.lower()
        w = w.strip(string.punctuation)
        if w == '' or w in stop_words:
            continue
        words.append(w)
    return words

def avg_syllables_per_word(words):
    total_syll = sum(count_syllables(w) for w in words)
    return total_syll / (len(words) + 1e-6)

