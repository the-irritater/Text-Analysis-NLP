import requests
from bs4 import BeautifulSoup
from newspaper import Article
from readability import Document

def fetch_article(url, timeout=10):
    # Try newspaper3k first
    try:
        art = Article(url)
        art.download()
        art.parse()
        title = art.title or ''
        text = art.text or ''
        if len(text.split()) > 30:
            return title.strip(), text.strip()
    except Exception:
        pass

    # Fallback: requests + readability
    try:
        resp = requests.get(url, timeout=timeout, headers={'User-Agent':'Mozilla/5.0'})
        resp.raise_for_status()
        doc = Document(resp.text)
        title = doc.short_title()
        cleaned_html = doc.summary()   # returns main content HTML
        soup = BeautifulSoup(cleaned_html, 'lxml')
        text = soup.get_text(separator='\n')
        if text:
            return title.strip(), text.strip()
    except Exception:
        pass

    # Last fallback: naive BeautifulSoup extraction
    try:
        resp = requests.get(url, timeout=timeout, headers={'User-Agent':'Mozilla/5.0'})
        soup = BeautifulSoup(resp.text, 'lxml')
        # Try common article tags
        article = soup.find('article')
        if article:
            title = (article.find('h1') or soup.find('h1')).get_text(strip=True)
            text = ' '.join(p.get_text() for p in article.find_all('p'))
            return title.strip(), text.strip()
        # generic fallbacks
        title = (soup.find('h1') or soup.title).get_text(strip=True)
        paragraphs = soup.find_all('p')
        text = ' '.join(p.get_text() for p in paragraphs)
        return title.strip(), text.strip()
    except Exception:
        return '', ''

