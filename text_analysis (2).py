import re

def sentiment_scores(tokens):
    pos = sum(1 for t in tokens if t in positive)
    neg = sum(1 for t in tokens if t in negative)
    polarity = (pos - neg) / ((pos + neg) + 1e-6)
    subj = (pos + neg) / (len(tokens) + 1e-6)
    return pos, neg, polarity, subj

